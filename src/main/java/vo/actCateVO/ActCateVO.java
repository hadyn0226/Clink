package vo.actCateVO;

public class ActCateVO {

	private int actcateNo;
	private String uuid;
	private String summary;
	private int bigcate;
	
	public ActCateVO() {
		super();
	}

	public int getActcateNo() {
		return actcateNo;
	}

	public void setActcateNo(int actcateNo) {
		this.actcateNo = actcateNo;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getSummary() {
		return summary;
	}

	public void setSummary(String summary) {
		this.summary = summary;
	}

	public int getBigcate() {
		return bigcate;
	}

	public void setBigcate(int bigcate) {
		this.bigcate = bigcate;
	}
	
	
	
	
}
